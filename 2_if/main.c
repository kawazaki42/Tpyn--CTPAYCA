#include <stdio.h>   /* Стандартный ввод-вывод: printf, scanf, puts */
#include <locale.h>  /* setlocale, LC_ALL */


/// @brief Главная процедура (функция) программы.
/// @return 0 при успешном выполнении программы
int main() {
    // Установить кодировку UTF-8
    // Локаль США (для разделителя-точки)
    setlocale(LC_ALL, "en_US.UTF8");

    // Ввод данных

    float x, y;  //< Входные данные: координаты точки

    puts("Принадлежит ли точка единичной окружности?");
    puts("Введите координаты точки");
    
    printf("x = ");
    scanf("%f", &x);

    printf("y = ");
    scanf("%f", &y);

    // Проверка условия и вывод

    // Основное тригонометрическое тождество.
    // Точка, для которой x^2 + y^2 = 1^2
    // - лежит _на_ окружности радиуса 1 и с центром в (0, 0).
    // Точка, для которой x^2 + y^2 < 1^2
    // - лежит внутри области (круга) этой же окружности
    if (x*x + y*y <= 1)
        // Если условие выполнено, то...
        puts("Принадлежит");
    else
        // Иначе...
        puts("Не принадлежит");

    return 0;
}